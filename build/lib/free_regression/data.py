def transpose(data:list) -> list:
    """
    Faz a transposição dos dados
    """
    new_data:list = []
    for i in range(len(data[0])):
        try:
            new_data.append([data[n][i] for n in range(len(data))])
        except IndexError:
            pass

    return new_data

